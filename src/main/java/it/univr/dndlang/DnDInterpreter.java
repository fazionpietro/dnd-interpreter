  /** 
   * Rolls two dice and keeps the highest (advantage) or lowest (disadvantage). 
   */
  @Override
  public Object visitDiceAdvDisExpr(DiceAdvDisExprContext ctx) {
    /* 
     * TEACHER: 
     * In Dungeons & Dragons, "Advantage" and "Disadvantage" are mechanics 
     * where you roll a die twice and take the higher or lower result, 
     * respectively. This represents favorable or unfavorable conditions.
     */
    int r1 = (int) visit(ctx.diceOnly());
    int r2 = (int) visit(ctx.diceOnly());

    boolean isAdv = ctx.ADV() != null;

    int res = isAdv ? Math.max(r1, r2) : Math.min(r1, r2);

    String type = isAdv ? "Advantage" : "Disadvantage";
    System.out.println(" > [" + type + "] Rolled " + r1 + " and " + r2 + " -> Keeping " + res);

    return res;
  }

  /** 
   * Evaluates a saving throw (save) or opposed check (vs) and returns a boolean. 
   */
  @Override
  public Object visitSaveVsExpr(SaveVsExprContext ctx) {
    /*
     * TEACHER:
     * A Saving Throw (SAVE) compares a rolled value against a static target 
     * number known as the Difficulty Class (DC). Meeting or exceeding the DC 
     * results in a success.
     * An Opposed Check (VS) compares a rolled value against an opponent's 
     * rolled value. The higher result wins.
     */
    Object left = visit(ctx.expr(0));
    Object right = visit(ctx.expr(1));
    double l = asDouble(left, ctx);
    double r = asDouble(right, ctx);

    boolean success;

    if (ctx.SAVE() != null) {
      success = l >= r;
      System.out.println(
          "  > [ Saving Throw ] Total: "
              + (int) l
              + " vs DC: "
              + (int) r
              + " -> "
              + (success ? "SUCCESS" : "FAILURE"));
      return success;
    } else if (ctx.VS() != null) {
      success = l > r;
      System.out.println(
          "  > [ Opposed Check ] Active: "
              + (int) l
              + " vs Opponent: "
              + (int) r
              + " -> "
              + (success ? "VICTORY" : "DEFEAT"));
      return success;
    } else {
      throw new DnDLangError("", ctx.getStart().getLine());
    }
  }

