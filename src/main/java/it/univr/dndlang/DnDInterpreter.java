  @Override
  public Object visitExprStmt(ExprStmtContext ctx) {
    visit(ctx.expr());
    return null;
  }

  /** 
   * Rolls the die specified by the diceOnly rule. 
   */
  @Override
  public Object visitDiceOnly(DiceOnlyContext ctx) {
    if (ctx.D20() != null) return rollDice(20);
    if (ctx.D12() != null) return rollDice(12);
    if (ctx.D10() != null) return rollDice(10);
    if (ctx.D8() != null) return rollDice(8);
    if (ctx.D6() != null) return rollDice(6);
    if (ctx.D4() != null) return rollDice(4);
    if (ctx.D3() != null) return rollDice(3);
    throw new DnDLangError("Unrecognized die", ctx.getStart().getLine());
  }

  /** 
   * Evaluates logical AND between two boolean expressions. 
   */
  @Override
  public Object visitAndExpr(AndExprContext ctx) {
    Object left = visit(ctx.expr(0));
    Object right = visit(ctx.expr(1));

    if (!(left instanceof Boolean) || !(right instanceof Boolean)) {
      throw new DnDLangError(
          "The '&&' operator requires two boolean values", ctx.getStart().getLine());
    }
    return (Boolean) left && (Boolean) right;
  }

  /** 
   * Evaluates logical OR between two boolean expressions. 
   */
  @Override
  public Object visitOrExpr(OrExprContext ctx) {
    Object left = visit(ctx.expr(0));
    Object right = visit(ctx.expr(1));

    if (!(left instanceof Boolean) || !(right instanceof Boolean)) {
      throw new DnDLangError(
          "The '||' operator requires two boolean values", ctx.getStart().getLine());
    }
    return (Boolean) left || (Boolean) right;
  }

  /** 
   * Evaluates a unary operator: logical NOT (!) or arithmetic negation (-). 
   */
  @Override
  public Object visitUnaryExpr(UnaryExprContext ctx) {
    Object value = visit(ctx.expr());

    if (ctx.NOT() != null) {
      if (!(value instanceof Boolean)) {
        throw new DnDLangError(
            "The '!' operator requires a boolean value", ctx.getStart().getLine());
      }
      return !(Boolean) value;
    } else if (ctx.MINUS() != null) {
      double num = asDouble(value, ctx);
      if (value instanceof Integer) {
        return (int) -num;
      }
      return -num;
    }
    throw new DnDLangError("Unrecognized unary operator", ctx.getStart().getLine());
  }

  /* --- Returns and Function Declarations --- */

  /** 
   * Throws a ReturnException to halt execution and return a value. 
   */
  @Override
  public Object visitReturnStmt(DnDLangParser.ReturnStmtContext ctx) {
    Object value = (ctx.expr() != null) ? visit(ctx.expr()) : null;
    throw new ReturnException(value, currentReturnType);
  }

  /** 
   * Registers a function in the environment with its name, return type, and AST node. 
   */
  @Override
  public Object visitFunctionDecl(DnDLangParser.FunctionDeclContext ctx) {
    String name = ctx.ID().getText();

    String returnType = ctx.getChild(1).getText();

    env.declareFunction(name, returnType, ctx);
    return null;
  }

  /* --- Control Flow (if, while, switch, break) --- */

  /** 
   * Evaluates the condition and executes either the then-block or the else-block. 
   */
  @Override
  public Object visitIfStmt(IfStmtContext ctx) {
    Object condition = visit(ctx.expr());

    if (!(condition instanceof Boolean)) {
      throw new DnDLangError(
          "The condition of the 'if' construct must be a boolean", ctx.getStart().getLine());
    }
    if ((Boolean) condition) {
      visit(ctx.block(0));
    } else if (ctx.block().size() > 1) {
      visit(ctx.block(1));
    }
    return null;
  }

  /** 
   * Executes the loop body until the condition is false or a break is encountered. 
   */
  @Override
  public Object visitWhileStmt(WhileStmtContext ctx) {
    while (true) {
      Object condition = visit(ctx.expr());

      if (!(condition instanceof Boolean)) {
        throw new DnDLangError(
            "The condition of the 'while' construct must be a boolean",
            ctx.getStart().getLine());
      }

      if (!(Boolean) condition) {
        break;
      }

      visit(ctx.block());

      if (isBreaking) {
        isBreaking = false;
        break;
      }
    }
    return null;
  }

  /** 
   * Matches the switch value with each case and executes the corresponding block. 
   */
  @Override
  public Object visitSwitchStmt(SwitchStmtContext ctx) {
    Object switchValue = visit(ctx.expr());

    for (DnDLangParser.CaseBlockContext caseCtx : ctx.caseBlock()) {
      Object caseValue = visit(caseCtx.expr());
      boolean isMatch = false;

      if (switchValue instanceof Number && caseValue instanceof Number) {
        isMatch = asDouble(switchValue, ctx) == asDouble(caseValue, ctx);
      } else {
        isMatch = switchValue.equals(caseValue);
      }

      if (isMatch) {
        visit(caseCtx.block());
        /* Reset the break flag after executing the corresponding case */
        isBreaking = false;
        return null;
      }
    }
    if (ctx.defaultBlock() != null) {
      visit(ctx.defaultBlock().block());
      isBreaking = false;
    }
    return null;
  }

  /** 
   * Activates the break flag to interrupt the current loop. 
   */
  @Override
  public Object visitBreakStmt(BreakStmtContext ctx) {
    isBreaking = true;
    return null;
  }

  /* --- Utility Methods --- */

  /** 
   * Returns the default value for a given type (0, false, ""). 
   */
  /*
   * CHECKLIST:
   * When implementing a new variable type, ensure you update BOTH 
   * `getDefaultValueForType()` and `coerceToDeclaredType()` 
   * to handle the type properly.
   */
  private Object getDefaultValueForType(String type, ParserRuleContext ctx) {
    return switch (type) {
      case "HP", "AC", "Int" -> 0;
      case "Gold" -> 0.0;
      case "Bool" -> false;
      case "String" -> "";
      default -> throw new DnDLangError("Unknown type: " + type, ctx.getStart().getLine());
    };
  }

  /** 
   * Coerces a value to the declared type, if compatible (e.g. Int -> Gold becomes Double). 
   */
  private Object coerceToDeclaredType(Object value, String declaredType) {
    /* Gold is always Double: convert an Integer to Double */
    if ("Gold".equals(declaredType) && value instanceof Integer i) {
      return i.doubleValue();
    }
    /* HP is an Integer, but it is bounded to 0 if it drops into negatives */
    if ("HP".equals(declaredType)) {
      int intVal;
      if (value instanceof Double d) intVal = (int) d.doubleValue();
      else if (value instanceof Integer i) intVal = i;
      else throw new IllegalArgumentException("Incompatible type for " + declaredType);

      return Math.max(intVal, 0);
    }
    /* AC is an Integer and cannot be negative */
    if ("AC".equals(declaredType)) {
      int intVal;
      if (value instanceof Double d) intVal = (int) d.doubleValue();
      else if (value instanceof Integer i) intVal = i;
      else throw new IllegalArgumentException("Incompatible type for " + declaredType);

      if (intVal < 0)
        throw new IllegalArgumentException(
            declaredType + " cannot be negative (value: " + intVal + ")");
      return intVal;
    }

    /* Int is obviously an Integer: truncate a Double */
    if ("Int".equals(declaredType) && value instanceof Double d) {
      return (int) d.doubleValue();
    }

    /* If no coercion is needed, simply verify type compatibility */
    boolean typeMatch =
        switch (declaredType) {
          case "Int", "HP", "AC" -> value instanceof Integer;
          case "Float", "Gold" -> value instanceof Double;
          case "Bool" -> value instanceof Boolean;
          case "String" -> value instanceof String;
          case "Void" -> value == null;
          default -> false;
        };
    if (!typeMatch) {

      throw new IllegalArgumentException(
          "Incompatible type: expected "
              + declaredType
              + ", received "
              + value.getClass().getSimpleName());
    }

    return value;
  }

  /** 
   * Executes a declarative block (hero/foe) without creating a new scope. 
   */
  private Object visitDeclarativeBlock(DnDLangParser.BlockContext ctx) {
    if (ctx.statement() != null) {
      for (var stmt : ctx.statement()) {
        visit(stmt);
        if (isBreaking) {
          throw new DnDLangError("'break' used outside of a loop", stmt.getStart().getLine());
        }
      }
    }
    return null;
  }

  /** 
   * Converts a numeric value to double, throwing an error if not numeric. 
   */
  private double asDouble(Object value, ParserRuleContext ctx) {
    if (value instanceof Number num) {
      return num.doubleValue();
    }
    throw new DnDLangError(
        "Math operation on a non-numeric type: " + value, ctx.getStart().getLine());
  }

  /** 
   * Verifies if both operands are integers. 
   */
  private boolean areBothIntegers(Object left, Object right) {
    return left instanceof Integer && right instanceof Integer;
  }

  /** 
   * Generates a random number between 1 and the number of faces of the die. 
   */
  private int rollDice(int sides) {
    return new java.util.Random().nextInt(sides) + 1;
  }

  /** 
   * Resolves an identifier, applying the struct prefix (hero./foe.) if present. 
   */
  private String resolveId(String rawId, int line) {
    /* 
     * Inside hero/foe blocks, we first attempt to resolve with the 
     * structural prefix (e.g. 'hero.hp'). 
     */
    if (!currentStructPrefix.isEmpty()) {
      String prefixedId = currentStructPrefix + rawId;
      if (env.contains(prefixedId)) {
        return prefixedId;
      }
    }

    /* If not found with the prefix, look for the global identifier */
    if (env.contains(rawId)) {
      return rawId;
    }

    throw new DnDLangError("Undeclared variable '" + rawId + "'", line);
  }
